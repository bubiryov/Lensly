    case lightItalic = "Nunito-LightItalic"
    case medium = "Nunito-Medium"
    case mediumItalic = "Nunito-MediumItalic"
    case regular = "Nunito-Regular"
    case semibold = "Nunito-SemiBold"
    case semiboldItalic = "Nunito-SemiBoldItalic"
}
